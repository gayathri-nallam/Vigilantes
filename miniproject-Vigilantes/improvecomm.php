<h3>3.Add pharses to your vocabulary </h3>
<p>
<div>1)	Write down new phrases </div>
<div> 2)	Learn collocations</div>
<div> 3) Use accurate adjectives and precise nouns</div>
<div> 4)	If you write like a six-year-old, regardless of how correct your writing is, it still is going to sound like a six-year-old. The only difference between a six-year-old with good grammar and a 20-year-old with good grammar is their vocabulary. So whenever you run into a phrase you want to start incorporating into your writing (or speech), write it down. And then make a point to use it.</div>
5)	A good idea is to start learning collocations. That's a fancy term for words that go together. "Get married" is useful, but "get married to someone" is even better -- that way you know not to say "get married with." If you said you "received a cold," you'd receive some funny looks -- but not if you said you "caught a cold." See how that works?
