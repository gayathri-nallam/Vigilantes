<h1><center>Presentation</center></h1>
<p><div>A presentation in the context of public speaking can be described as an activity where a presenter presents his/her ideas, explains a process, provides information, or raises questions regarding a subject for public debate. There can be many definitions of a presentation, however, it can be basically narrowed down to the aforementioned.</div></p>
<h2>Different Types of Presentations</h2>
<p><div>Presentations can be presented for different purposes. Before presenting a presentation, it is necessary that you have a clear idea regarding the purpose of the presentation. Below is an overview of some of the 5 most common types of presentations:</div></p>
<h3>1. Informative</h3>
<p><div>You might need to create an informative presentation to explain important details tied to a topic before an audience. This type of presentation might be brief, with essential information. Such a presentation is usually based on facts and avoids too many complicated details and assumptions.</div>
<div>Examples: Class lectures, research findings, technical information, results from experiments, etc.</div>
</p>
<h3>2. Persuasive</h3>
<p><div>A persuasive presentation is geared towards convincing the audience to believe a specific point of view. Such a presentation might conclude with a call to action.</div>
<div>Examples: Product demos, sales pitch, investor pitch, presentations on social issues, debates, etc.</div>
</p>
<h3>3. Instructional</h3>
<p><div>Such a presentation might be to provide an audience with instructions, such as regarding a process, or the use of a product. Such presentations are usually longer, as they require demonstrations and detailed explanation of each aspect of the topic.</div></p>
<h3>4. Arousing</h3>
<p><div>This type of presentation is meant to make the audience think about a certain topic. This can be to appeal to the intellect and emotions of the audience to point them towards a certain point of view or to start a social debate.</div>
<div>Example: Religious speech, presentation about a taboo, motivational presentation, etc.</div>
</p>
<h3>5. Decision Making</h3>
<p><div>Some presentations are conducted with the sole aim of providing facts and figures to help the audience reach a decision. This might include a business presentation about say, market share, profits, project revenue and market competition; so that the board members might be able to decide a future course of action for the company.</div>
<div>Examples: Business meetings, presentation about legislation and laws, a SWOT analysis presentation, etc.</div>
</p>
<h2>Different Means of Presenting Presentations</h2>
<p><div>How a presentation is presented can be roughly divided into 3 main categories, i.e. oral, visual only oral and visual.</div></p>
<h3>1. Oral Only</h3>
<p><div>This might simply include a presenter speaking directly to the audience, without any visual aid. This type of presentation can be difficult, since it can be hard to grab audience attention without any visual aid. If you are looking to give an oral presentation, you must have very good presentation skills and a compelling story to tell your audience. In fact, your audience is likely to be more interested in your presentation if you can present your ideas, concepts and explanations like a story.</div>
<div>While it might seem unlikely that someone would resort to an audio only presentation, however, this too can be quite an effective method for creating a sense of mystery of making your audience pay more attention to the content of your presentation. Such a presentation might be presented in the form of a story, read out or played via an audio device.</div></p>
<h3>2. Visual Only</h3>
<p><div>This might include a set of images or a video playing out before an audience. Such a presentation might include slides or a video clip with text that the audience requires to read to understand the visuals, a silent animation with a symbolic message, a set of images played like a slideshow, and the like</div></p>
<h3>3. Visual & Oral</h3>
<p><div>This is the most common method of presenting a presentation. Whether it is a presenter directly or remotely presenting a presentation, or a video presentation playing out before an audience, the use of both visual and an oral explanation is usually the most effective method for presenting a presentation. Such a presentation may also include background music, and audio effects to make the presentation more compelling.</div>
<div>Other than presentation software, and the use of devices like projectors, monitors and TV screens, visual and oral presentations might also include photographs, whiteboards, and handouts.</div></p>
<h2>The 5W’s of Effective Presentation</h2>
<p></p>
<h3>WHO AM I SPEAKING WITH?</h3>
<p><div>There’s an old saying for effective presentation preparation: Read the room. While the phrasing is a little odd, it means you must know who you’re speaking to.</div>
<div>Begin by researching your attendees. Are they young or old? Local or international? Casual or professional? Look into who’s likely to attend, but don’t stress about learning everything about them.</div>
<div>Once you have a good idea of who you’re talking to, turn that into an understanding of how they talk. This step is more about knowing what not to say than anything else. To stay safe, avoid statements which may be divisive or difficult to understand. Let’s say you want to make a reference which people might not get. Just drop it and pick another one. Trust us, that’s a lot easier than wasting time explaining it.</div>
</p>
<h3>WHAT TOOLS WILL I USE?</h3>
<p><div>The technology you use to present can either be  your  best friend or your worst enemy. It all depends on how well you practice presenting with it. For instance, if you have to use an unfamiliar projector, ask to see your slides on it before you begin. This will show you anything that needs adjusting, including incompatible colours and overblown images.</div>
<div>On top of that, always try to present off a device you own. There’s nothing worse than building an amazing custom presentation, then discovering those fonts don’t work on another device.
</div></p>
<h3>WHERE AM I PRESENTING?</h3>
<p><div>This is one of the most important parts of effective presentation preparation. Are you presenting to a small, quiet room? A large, loud hall? Wherever you’re presenting, put time into studying it. Ask if you can see a floor plan or technical specifications. Try to organise a rehearsal in the space, and make sure you can see your slides from the back row.</div></p>
<h3>WHEN SHOULD  I  STOP?</h3>
<p><div>We’ve all endured slideshows that stretched into eternity. To avoid this torture, you need to understand when to stop.</div>
<div>Understanding when to stop starts with an effective presentation structure. To start your search, we’ve outlined one of our favourite ten minute presentation structures in another blog post</div>
</p>
<h3>WHY AM I PRESENTING?</h3>
<p><div>If you can’t answer the question of why you’re presenting, don’t bother presenting at all. We know that sounds a little harsh, but why present without a purpose?</div>
<div>To avoid a pointless presentation, ask yourself what you want your audience to take away from what you say. Establish one clear call to action for them to follow, and you’ll find your purpose.</div>
</p>
