<h2>How to use body language during presentation</h2>
<p><div>How to use body language during presentation</div></p>
<h3>1) Facial expressions</h3>
<p><div>People will travel half-way around the world to meet one another “face-to-face” for a reason – when it comes to interacting with others, what we do with our faces is vital. We may not usually control our facial expressions in any conscious way, but there are times when we have to think about what our face is telling others, such as when giving a presentation</div></p>
<p><div>The first and most obvious thing to remember is to make sure that you are using your face at all. Giving a presentation with a blank face, without any particular facial expression is like speaking in a monotone – no matter how great your content is, your audience will not be engaged. Even some simple steps from the outset, such as opening your eyes wider, raising your eyebrows a little, and smiling, can make a huge difference in setting the tone for your presentation. You can also “reset” at different points during your presentation to make sure that you haven’t fallen back into a dull resting expression and to re-engage your audience’s attention.</div></p>
<h3>2) Eye contact</h3>
<p><div>a.	Make sure you look at everyone </div>
<div>b.	Don’t be afraid of eye contact </div>
<div>c.	Don’t stare at one person only</div></p>
<h3>3) Posture</h3>
<p><div>a.	DON’T slouch </div>
<div>b.	DON’T be tense </div>
<div>c.	DO think about your audience</div> 
<div>d.	DO think about your audience</div> </p>
<h3>4) Gestures</h3>
<p><div>Varied facial expressions, eye contact and a good posture will put you well on the way to presentation success, but if you stand still without moving any other part of your body, it can create a very strange impression. On the other hand, over-rehearsed or exaggerated hand gestures can be off-putting and look unnatural.</div></p>
<h3>5) Position and movement</h3>
<p><div>This last area is more variable depending on the specific set-up of your presentation. It will be clear straight away whether you have any flexibility over where you position yourself or if movement around the space is even possible, but it’s always worth considering.</div></p>
<p><div>For example, if you are giving your presentation on a big stage, a bit of movement around the space can help to create visual interest and keep different parts of the audience engaged. Likewise, if your presentation has interactive elements, you could move closer or slightly further back from the audience depending on whether they’re involved or not. The golden rule is that any movement should be clear and directed – you should never look like you’re just wandering around the stage. You may, for example, want to engage your audience early on in your presentation by moving to the front of the stage and asking them a question – “Who can tell me…”, “Put your hand up if you have ever…” – this not only enables you to make some judgements about how much your audience already knows about what you have to say, it also engages them and suggests that you care about their experiences. Most people are much happier if they feel a speaker is “talking to” them rather than “talking at” them with no concern for their opinions.</div></p>
