<h2>What is Telephone Conversation? </h2>
<p>What is Telephone Conversation or How to Make Effective Telephone Calls. One of the oldest and widely used mechanical media of oral communication is telephone.</p>
<p>By telephone persons residing far away from each other can easily exchange necessary information. By it, urgent message can be transmitted and prompt feedback can be ensured. Definition of telephone conversation, Meaning of telephone conversation.</p>
<p>We can define telephonic conversation as an exchange of information between two persons over telephone. This is not a face-to-face conversation rather a person-to-person conversation where nobody sees other but hears each other and interacts instantly.</p>
<p>Telephonic conversation is most effective when distance is longer and time is a great factor. Now-a-days cell phones are becoming more popular along with land phone as mechanical media of oral communication.</p>


<h2>How to start a telephonic conversation? </h2>
<p>Introduce yourself. English telephone conversations almost always start in the same way 
<div> by introducing yourself. Say “Hello, this is (name)” to let people know who you are. If you answer the 
phone and the caller doesn't give his name, you can say “May I ask who's calling, please?”.</div>
</p>

<h3>How to Make Effective Telephone Calls </h3>

<p>Talking over telephone is an art. Good telephoning techniques can help you win friends and create goodwill. You voice may convey a first impression of your business or organization.
There is an old proverb “you do not get a second chance to make a first impression,” which is fully applicable in telephone conversation. Good telephone cells require proper planning the message before dialing the number.

</p>

<h2> How to Make Effective Telephone Calls</h2>

<h3>The following suggestions are recommended to follow while making telephone calls- </h3>
<p> 1)	Make the call at an appropriate time. 9.00 am to 5.00 p.m. is appropriate time for office or office or business calls and 9.00 am to 9.00 p.m. is for personal calls. Be careful of time difference when making international calls.</p>
<p>2)	 Make sure about the number and dial it correctly.</p>
<p> 3)	Give the call receiver enough time to answer the call, up to ten rings.</p>
<p>4)	Smile and provide details about your identity like name, company, department, etc.</p>
<p>5)	Identify clearly the person you want to talk to.</p>
<p>6)	Share greetings and provide the reason of your calling.</p>
<p>7)	Be careful and courteous when delivering the message, and try to be sure that the receiver understands you.</p>
<p>8)	Be patient when calling with a problem. You are to explain the reason several times to make your counterpart understand the difficulty.</p>
<p>9)	Give complete information to your listener if you want him call you back.</p>
<p>10)	If you do not reach the intended person and want your call returned, leave a complete message including your name, number, organization, etc.</p>
<p>11)	Make sense when you are to end the conversation. Do not forget to offer “Thanks” and “Good-bye” at the end of the call.</p>
<p>12)	Be considerate of other who may share your phone.</p>

