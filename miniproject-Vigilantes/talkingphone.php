<h2> Have Phrases for Telephone Calls in English</h2>


<h3>1.Answering the Phone</h3>
<h4>Formal </h4>
<p>
<div>a)	Hello. This is Susan. (Personal)</div>
<div>b)	Hello, thank you for calling [name of company]. This is Susan – how can I help you? (Professional)</div>
c)	Good morning (good afternoon), this is Susan. How can I help you? (Professional)
</p>
<h4> Informal</h4>
<p>
<div>a)	Hello.</div>
b)	Susan speaking.
</p>

<h3>2.Greeting and Introducing Yourself </h3>
<h4> Formal</h4>
<p>
<div>a)	This is Laure from [company name].</div>
b)	Hello. This is Laure. I am calling about… (your advertisement, information about your services, to make a reservation, etc.)
</p>
<h4> Informal</h4>
<p>
<div>a)	Hi. It’s Laure.</div>
b)	Hi – it’s me. (Use this when the other person will recognize your voice. For example, your mother, your best friend, or your brother.)
</p>

<h3>3. Asking to Speak to Someone</h3>
<h4> Formal</h4>
<p>
<div>a)	Is _____________ available? (Example: Is Kim available?)</div>
<div>b)	May I speak to ________________? (Example: May I speak to the doctor?)</div>
c)	I’d like to talk with _____________. (Example: I’d like to talk with Mr. Adams.)
</p>
<h4>Informal </h4>
<p>
<div>a)	Is _______ free? (Example: Is Çim free?)</div>
b)	Can I talk to ______? (Example: Can I talk to mom?)
</p>

<h3>4.Putting Someone on Hold </h3>
<h4> Formal</h4>
<p>
<div>a)	Can I put you on hold for a moment, please?</div>
<div>b)	Would you mind holding for just a moment?</div>
c)	If it is ok with you, I am going to put you on hold for a moment. I will see if she is available.</div>
??Pro Tip: These expressions can also be used when you are not ready to speak in English on the phone. You can use this moment to calm yourself and prepare yourself to speak English.
</p>
<h4> Informal</h4>
<p>
<div>a)	Hold on a sec. (Note: “Sec” is short for “second” and is another way to say “Hold on a moment.”)</div>
<div>b)	Just a sec.</div>
c)	Just a moment.
</p>

<h3>5.Clarifying and Asking for Repetition </h3>
<h4> Formal</h4>
<p>
<div>a)	I’m sorry – I didn’t catch that. Could you please repeat yourself?</div>
<div>b)	I’m sorry – do you mean to say [identify what you understand]?</div>
<div>c)	Just to clarify, you said… (Example: Just to clarify, you said you were calling from RTR Industries?)</div>
<div>d)	Would you mind spelling that for me?</div>
<div>e)	Would you mind slowing down?</div>
f)	Would you mind speaking a little more slowly?

</p>
<h4> Informal</h4>
<p>
a)	I’m sorry. Could you repeat that?</div>
b)	Can you spell that?

</p>
<h3> Taking a Message for Someone</h3>
<h4> Formal</h4>
<p>
a)	I’m sorry. She isn’t available at the moment. Can I take a message for her?</div>
b)	If you leave a message, I will be sure to get it to him as soon as he is available.</div>
c)	She is away at the moment. May I ask who is calling?</div>
d)	Would you like me to connect you to his voicemail?

</p>
<h4> Informal</h4>
<p>
a)	I’ll let him/her know that you called.</div>
b)	Who’s calling, please?

</p>
<h3> Leaving a Message for Someone</h3>
<h4> Formal</h4>
<p>
<div>a)	Yes. Thank you. Please tell her Selma called. Ask her to call me back at 430.444.4444.</div>
<div>b)	Could you have her return my call tomorrow?</div>
<div>c)	Please tell him I’ll be available on Thursday at 9:00.</div>
d)	Yes, please put me through to her voicemail. Thank you.
</p>
<h4> Informal</h4>
<p>
<div>a)	No thanks. I’ll call back later.</div>
b)	Yeah, tell him Selma called.
</p>

<h3> Ending Your Call</h3>
<h4>Formal </h4>
<p>
<div>a)	Thank you for calling. I will make sure to give him the message.</div>
<div>b)	Thanks for calling – I have another phone call so I will need to let you go.</div>
<div>c)	Thanks for calling and have a great day.</div>

</p>
<h4>Informal </h4>
<p>
<div>a)	I need to let you go.</div>
<div>b)	I have a meeting soon so I have to run. (Americans would also say: “I have a meeting soon so I gotta run. “Gotta” is short for “got to” or “have to.“)</div>
c)	It was great to chat. I’ll talk to you again soon.

</p>
