<h2>Directional business communication:</h2>
<h3>a.	Top-down communication:</h3>
<p><div>	Communication within the business that is passed down from the top of the business hierarchy to the bottom, the amount of information passed from one level to another relies on a "need to know" basis.</div></p>
<h3>b.	Bottom-up communication:</h3>
<p><div>	Communication within the business that is passed up through the business hierarchy from the bottom up.</div></p>
<h3>c.	Horizontal communication:</h3>
<p><div>Communication with those who are on the same level in the business hierarchy as one, very little information moves up or down the chain of command.</div></p>
<h2>Elements of Effective Business Communication:</h2>
<p><div>a.	Structure: the structure of your communication is important because it dictates how it is absorbed and understood by your target market. A good communication is characterised by three structural elements: an opening, a body, and a close. Whether it is a phone call, a memo, or a voice call, the three structural elements have to be applied to any form of communication.
</div><div>b.	Clarity: when communicating, make sure that your message is clear. An unclear message is bound to leave your audience confused and they will ignore your message.
</div><div>c.	Consistency: consistency is important when relaying your message to your audience. Consistency creates trust and makes your message effective. Ensure that your tenses are the same, that your point of view does not wander between the first and third person and that your general message does not change.
</div><div>d.	Medium: you can deliver your message through different media. However, ensure that you the right one. In other words, relay your message with utmost accuracy and clarity while spending as little money and time as possible.
</div><div>e.	Relevance: “the psychological reality is that unless a person is interested in the subject of the message, they are highly unlikely to pay any attention.” Ergo, ensure that your message is relevant to your target audience for them to pay attention.
</div><div>f.	Recency/primacy: a business communication is remembered by the power and memorability of its opening and its close.
</div></p>
